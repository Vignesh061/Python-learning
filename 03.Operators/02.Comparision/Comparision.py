# Comparisoin Operators

"""Symbols 
==, !=, <, >, <=, >=
"""
a=10
b=2

print(" a Equal to b :",a==b)                           #False
print("a Not Equal b : ",a!=b)                          #True
print(" a Less then b : ",a<b)                          #False
print(" a Greter then b :",a>b)                         #True
print("a Less the are equal to b :",a<=b)               #False
print("a Greater then are equal to b :",a>=b)           #True
