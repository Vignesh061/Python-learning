# Arithmetoc Operators 
""" Symbols
 +, -, *, /, //, %, **
"""

a=10
b=4

print("Addition:",a+b)          # 14
print("Subtraction:",a-b)       # 6
print("Multiplication:",a*b)    # 40
print("Division:",a/b)          # 2.5
print("Floor Divison:",a//b)    # 2
print("Modulus:",a%b)           # 2
print("Power:",a**b)            # 10000
